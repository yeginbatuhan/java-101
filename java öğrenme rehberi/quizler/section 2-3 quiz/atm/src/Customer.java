public class Customer {
    String tcno;
    String parola;
    String ad;
    String soyad;
    int bakiye;
    int islemGecmisi;


    public Customer(String tcno, String parola, String ad, String soyad, int bakiye, int islemGecmisi) {
        this.tcno = tcno;
        this.parola = parola;
        this.ad = ad;
        this.soyad = soyad;
        this.bakiye = bakiye;
        this.islemGecmisi = islemGecmisi;
    }
}
